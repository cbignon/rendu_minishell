#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	x;
	char	*t;

	x = 0;
	t = (char *)s;
	while (x < n)
	{
		t[x] = (unsigned char)c;
		x++;
	}
	return ((void *)s);
}
