#include "ft_def.h"

int64_t	ft_atoll(char *str)
{
	int			x;
	char		z;
	int64_t		r;

	x = 0;
	z = 1;
	r = 0;
	while (str[x] == ' ' || str[x] == '\t' || str[x] == '\v'
		|| str[x] == '\f' || str[x] == '\r' || str[x] == '\n')
		x++;
	if (str[x] == '-')
	{
		z *= -1;
		x++;
	}
	else if (str[x] == '+')
		x++;
	while (str[x] >= '0' && str[x] <= '9')
		r = r * 10 + str[x++] - '0';
	r *= z;
	return (r);
}
