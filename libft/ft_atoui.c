#include "libft.h"

unsigned int	ft_atoui(char *str)
{
	int				x;
	unsigned int	r;

	x = 0;
	r = 0;
	while (str[x] >= '0' && str[x] <= '9')
		r = r * 10 + str[x++] - '0';
	return (r);
}
